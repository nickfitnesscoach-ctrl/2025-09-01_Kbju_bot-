def get_text(key: str, **kwargs) -> str:
    """–ü–æ–ª—É—á–∏—Ç—å —Ç–µ–∫—Å—Ç –ø–æ –∫–ª—é—á—É —Å –ø–æ–¥—Å—Ç–∞–Ω–æ–≤–∫–æ–π –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤"""
    # –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –ø–µ—Ä–µ–∑–∞–≥—Ä—É–∂–∞–µ–º —Ç–µ–∫—Å—Ç—ã –ø—Ä–∏ –∫–∞–∂–¥–æ–º –≤—ã–∑–æ–≤–µ
    load_texts()
    
    keys = key.split('.')
    text_data = TEXTS
    
    for k in keys:
        if isinstance(text_data, dict) and k in text_data:
            text_data = text_data[k]
        else:
            return f"[–¢–µ–∫—Å—Ç –Ω–µ –Ω–∞–π–¥–µ–Ω: {key}]"
    
    if isinstance(text_data, dict) and 'text' in text_data:
        text_data = text_data['text']
    
    if isinstance(text_data, str):
        try:
            return text_data.format(**kwargs)
        except KeyError:
            return text_data
    
    return str(text_data)


